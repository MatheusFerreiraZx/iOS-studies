// para fazer definir o tipo da variável, se usa o seguinte formato:
var mensagem: String = "Seja bem-vindo ao Swift"

print(mensagem)

// voce também pode definir tipo para variaveis de uma vez só:
var nome: String, idade: Int

nome = "João"
idade = 23

print(nome)
print(idade)

// e tambem o mesmo tipo para varias variaveis ao mesmo tempo.
var nome2, sobrenome, nomeCompleto: String

nome2 = "Maria"
sobrenome = "Silva"
nomeCompleto = nome2 + " " + sobrenome

print(nomeCompleto)